function changeLanguage(lang) {
    if(!translations[lang]) return;
    
    // Guardar preferencia
    localStorage.setItem('pref_lang', lang);
    
    document.documentElement.lang = lang;
    
    // Reemplazar textos normales (<p>, <span>, <h1>, etc) o placeholders
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = translations[lang][key];
        
        if(translation) {
            // Verificar si es un input para traducir su placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', translation);
                } else if (el.type === 'button' || el.type === 'submit') {
                    el.value = translation;
                }
            } else if (el.tagName === 'OPTION') {
                el.textContent = translation;
            } else {
                // Traducir innerHTML para permitir etiquetas como <strong> y <br>
                el.innerHTML = translation;
            }
        }
    });

    // Actualizar selectores visuales si existe selector de idioma
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        if(btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Iniciar al cargar
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('pref_lang') || 'es';
    changeLanguage(savedLang);

    // Escuchar clicks de botones de idioma
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = btn.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });
});
